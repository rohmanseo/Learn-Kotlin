package Rohman.BasicTypes

fun main(){

    val one = 1
    val threeBillion = 3000000
    val oneLong = 1L
    val oneByte: Byte = 1

    println(one)
    println(threeBillion)
    println(oneLong)
    println(oneByte)

    //Underscore in numeric literal
    val oneMillion = 1_000_000

    println(oneMillion)
}